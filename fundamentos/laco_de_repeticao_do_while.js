let i = 1;
do {
    console.log('digitando...', i);
    i++;
} while (i <= 10);

// Imprima o comando "digitando..." no console de forma crescente enquanto 'i' for menor igual a 10.

// Nota: A diferença entre 'Do..While' e 'While loop' é que o primeiro será executado pelo menos uma vez e só irá verificar a
// condição depois de já ter visto uma vez. 'While loop' verifica primeiro e executa depois.