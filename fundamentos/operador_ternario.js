// OPERADOR TERNÁRIO
// O operador condicional é o único operador JavaScript que utiliza três operandos. O operador pode ter um de 
// dois valores baseados em uma condição.

let pontos = 101;
let tipo = (pontos > 100) ? 'premium' : 'comum';
console.log(tipo);

// Outro exemplo:
var idade = 18;
var status = (idade >= 18) ? 'adulto' : 'menor de idade'; 
console.log(status);

