/*Escreva um código que imprima no console "Bom dia!" caso a hora informada seja entre 6 e 11, "Boa tarde!" caso entre 12 e 17 ou
caso contrário "Boa noite!".
utilizando as condicionais If e Else para a*/

let hora = 5;

if (hora >= 6 && hora <= 11) {
    console.log('Bom dia!');
}

else if (hora >= 12 && hora <= 17) {
    console.log('Boa tarde!');
}

else {
    console.log('Boa noite!');
}