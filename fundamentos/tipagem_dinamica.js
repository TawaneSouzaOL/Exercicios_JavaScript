/* Tipagem dinâmica
JavaScript é uma linguagem de tipagem dinâmica. Isso significa que você não necessita declarar o tipo
de uma variável antes de sua atribuição. O tipo será automaticamente determinado quando o programa 
for processado. Isso também significa que você pode reatribuir uma mesma variável com um tipo 
diferente:*/

var foo = 42;    // foo é um Number agora
foo     = "bar"; // foo é um String agora
foo     = true;  // foo é um Boolean agora